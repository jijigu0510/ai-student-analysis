  function cleanAIJsonResponse(text) {
    let jsonString = text.trim();

    // 1. Markdown Code Block 제거
    if (jsonString.startsWith("```")) {
      const match = jsonString.match(/^```(?:json)?\s*([\s\S]*?)\s*```/);
      if (match) jsonString = match[1].trim();
    }

    // 2. 가장 바깥쪽 {} 찾기 (불필요한 텍스트 제거)
    const startIdx = jsonString.indexOf('{');
    const endIdx = jsonString.lastIndexOf('}');
    if (startIdx !== -1 && endIdx !== -1 && endIdx >= startIdx) {
      jsonString = jsonString.substring(startIdx, endIdx + 1);
    }

    // 3. JSON 구조 보정: 따옴표 내부의 실제 줄바꿈(Literal newline)을 \n으로 치환
    jsonString = jsonString.replace(/"([^"]*?)"/gs, (match, p1) => {
      return '"' + p1.replace(/\n/g, '\\n').replace(/\r/g, '\\r') + '"';
    });

    return jsonString;
  }
